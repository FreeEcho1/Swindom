namespace Swindom;

/// <summary>
/// 最前面の種類
/// </summary>
public enum Forefront : int
{
    /// <summary>
    /// 変更しない
    /// </summary>
    DoNotChange,
    /// <summary>
    /// 常に最前面解除
    /// </summary>
    Cancel,
    /// <summary>
    /// 常に最前面
    /// </summary>
    Always,
    /// <summary>
    /// 最前面
    /// </summary>
    Forefront
}
