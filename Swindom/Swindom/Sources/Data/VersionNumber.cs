namespace Swindom;

/// <summary>
/// バージョン番号
/// </summary>
public class VersionNumber
{
    /// <summary>
    /// メジャー番号 (ない「-1」)
    /// </summary>
    public int MajorNumber = -1;
    /// <summary>
    /// マイナー番号 (ない「-1」)
    /// </summary>
    public int MinorNumber = -1;
    /// <summary>
    /// ビルド番号 (ない「-1」)
    /// </summary>
    public int BuildNumber = -1;
    /// <summary>
    /// プライベート番号 (ない「-1」)
    /// </summary>
    public int PrivateNumber = -1;
}
